function D = generateDifferenceMatrix(n)
    % 初始化一个 n x n 的零矩阵
    D = zeros(n, n);
    
    % 填充矩阵的上三角部分（不包括对角线）为 1
    % 填充矩阵的下三角部分（不包括对角线）为 -1
    % 对于第一行和最后一列进行特殊处理，以实现循环差分
    for i = 1:n
        for j = 1:n
            if i == j
                % 对角线元素为 0（对于标准的差分矩阵，对角线通常为 0）
                D(i, j) = 0;
            elseif i < j
                % 上三角部分（不包括对角线）为 1
                D(i, j) = 1;
            else
                % 下三角部分（不包括对角线）为 -1
                % 对于第一行，需要循环到最后一列（实现循环差分）
                % 对于最后一列，需要循环到第一行（实现循环差分）
                if i == 1 && j == n
                    D(i, j) = -1; % 特殊处理第一行到最后一列的循环差分
                elseif j == 1 && i == n
                    D(i, j) = 1; % 特殊处理最后一列到第一行的循环差分（与上三角部分对应）
                else
                    D(i, j) = -1;
                end
            end
        end
    end